

age_of_the_user = 50

if age_of_the_user <= 4:
    print("the user is an infant")
elif 4 < age_of_the_user <= 12:
    print("the user is a child")
elif 12 < age_of_the_user <= 19:
    print("the user is a teenager")
elif 19 < age_of_the_user <= 65:
    print("the user is an adult")
elif age_of_the_user >= 65:
    print("the user is an senior citizen")
